package com.vortex.domain.dto;

import java.time.LocalDateTime;

public class OrderDTO {

    private Long id;
    private Long total;

    private AddressDTO shipping;
    private AddressDTO billingAddress;

    // Solo los datos relevantes del usuario
    private Long userId;
    private String username;

    // Solo los datos relevantes del m√©todo de pago
    private Long paymentMethodId;
    private String paymentMethodName;

    // ID de la venta asociada
    private Long saleId;

    private LocalDateTime created_at;

    public OrderDTO() {}

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getTotal() {
        return total;
    }

    public void setTotal(Long total) {
        this.total = total;
    }

    public AddressDTO getShipping() {
        return shipping;
    }

    public void setShipping(AddressDTO shipping) {
        this.shipping = shipping;
    }

    public AddressDTO getBillingAddress() {
        return billingAddress;
    }

    public void setBillingAddress(AddressDTO billingAddress) {
        this.billingAddress = billingAddress;
    }

    public Long getUserId() {
        return userId;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public Long getPaymentMethodId() {
        return paymentMethodId;
    }

    public void setPaymentMethodId(Long paymentMethodId) {
        this.paymentMethodId = paymentMethodId;
    }

    public String getPaymentMethodName() {
        return paymentMethodName;
    }

    public void setPaymentMethodName(String paymentMethodName) {
        this.paymentMethodName = paymentMethodName;
    }

    public Long getSaleId() {
        return saleId;
    }

    public void setSaleId(Long saleId) {
        this.saleId = saleId;
    }

    public LocalDateTime getCreated_at() {
        return created_at;
    }

    public void setCreated_at(LocalDateTime created_at) {
        this.created_at = created_at;
    }
}
